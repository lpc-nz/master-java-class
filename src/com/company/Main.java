package com.company;

public class Main {

    public static void main(String[] args) {
	// Practice Switch statement
        int myNumber = 1;

        switch (myNumber){
            case 1:
                System.out.println("My number is 1 ");
                break;
            case 2:
                System.out.println("My number is 2");
                break;
            case 3:
                System.out.println("My number is 3");
                break;
            case 4:
                System.out.println("My number is 4");
                break;
            case 5:
                System.out.println("My number is 5");
                break;
            default:
                System.out.println("Can not find number");

        }
    }
}
